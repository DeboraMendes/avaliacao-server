-- CRIAR TABELAS
CREATE TABLE endereco
(
	ID INTEGER AUTO_INCREMENT, PRIMARY KEY (ID),
	RUA VARCHAR(800) NOT NULL,
	NUMERO INTEGER,
	BAIRRO VARCHAR(800) NOT NULL,
	CEP VARCHAR(9) NOT NULL,
	COMPLEMENTO VARCHAR(800) NOT NULL,
    ID_CIDADE INTEGER
);

CREATE TABLE empresa
(
	ID INTEGER  AUTO_INCREMENT, PRIMARY KEY (ID),
	NOME_FANTASIA VARCHAR(800) NOT NULL,
	RAZAO_SOCIAL VARCHAR(800) NOT NULL,
	EMAIL VARCHAR(800),
    TELEFONE VARCHAR(11),
	CAMINHO_FOTO VARCHAR(800),
	ID_ENDERECO INTEGER
);

CREATE TABLE estado
(
	ID INTEGER  AUTO_INCREMENT, PRIMARY KEY (ID),
	CODIGO_IBGE INTEGER NOT NULL,
	NOME VARCHAR(800) NOT NULL,
	SIGLA VARCHAR(2) NOT NULL
);

CREATE TABLE cidade
(
	ID INTEGER  AUTO_INCREMENT, PRIMARY KEY (ID),
	NOME VARCHAR(800) NOT NULL,
	ID_ESTADO INTEGER NOT NULL
);


CREATE TABLE historico_do_preco_da_passagem
(
	ID INTEGER AUTO_INCREMENT, PRIMARY KEY (ID),
	PRECO DOUBLE NOT NULL,
    DATA_DA_ALTERACAO DATE,
    ID_EMPRESA INTEGER
    
);

CREATE TABLE linha
(
	ID INTEGER AUTO_INCREMENT, PRIMARY KEY (ID),
    ID_EMPRESA INTEGER NOT NULL,
	DESCRICAO VARCHAR(800) NOT NULL
);

CREATE TABLE tipo_ponto
(
	ID INTEGER AUTO_INCREMENT, PRIMARY KEY (ID),
	DESCRICAO VARCHAR(800) NOT NULL
);

CREATE TABLE ponto
(
	ID INTEGER AUTO_INCREMENT, PRIMARY KEY (ID),
	LATITUDE DECIMAL NOT NULL,
	LONGITUDE DECIMAL NOT NULL,
	NOME VARCHAR(800)NOT NULL,
    DESCRICAO VARCHAR(800) NOT NULL,
    ID_TIPO_PONTO INTEGER NOT NULL,
    CAMINHO_FOTO VARCHAR(800)
);

CREATE TABLE linha_ponto
(
	ID INTEGER AUTO_INCREMENT, PRIMARY KEY (ID),
    ID_LINHA INTEGER NOT NULL,
	ID_PONTO INTEGER NOT NULL
);

